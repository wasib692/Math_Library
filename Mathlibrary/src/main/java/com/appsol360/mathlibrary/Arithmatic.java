package com.appsol360.mathlibrary;

public class Arithmatic {
    public static float Sum(double a, double b){
        double c=a+b;
        return (float) c;
    }
    public static float Subtract(double a, double b){
        double c=a-b;
        return (float) c;
    }
    public static float Divide(double a, double b){
        double c=a/b;
        return (float) c;
    }
    public static float Multiply(double a, double b){
        double c=a*b;
        return (float) c;
    }
}
